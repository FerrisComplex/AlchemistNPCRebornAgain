namespace AlchemistNPCRebornAgain.Buffs
{
	public class CloakOfFear : ModBuff
	{
		public override void SetStaticDefaults()
		{
			Main.debuff[Type] = true;
			Main.pvpBuff[Type] = true;
			Main.buffNoSave[Type] = false;
            
        }

		public override void Update(Player player, ref int buffIndex)
		{
			if (player.ownedProjectileCounts[ModContent.ProjectileType<Projectiles.CloakOfFear>()] == 0)
			{
				for (int h = 0; h < 1; h++) {
				Vector2 vel = new Vector2(0, -1);
				vel *= 0f;
				Projectile.NewProjectile(((Entity) player).GetSource_FromThis((string) null),player.Center.X, player.Center.Y, vel.X, vel.Y, ModContent.ProjectileType<Projectiles.CloakOfFear>(), 0, 0, player.whoAmI);
				}
			}
			if (player.buffTime[buffIndex] == 1) 
			{ 
				player.AddBuff(ModContent.BuffType<Exhausted>(), 1800); 
			}
		}
	}
}
